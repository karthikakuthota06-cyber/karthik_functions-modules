import math as m			# Importing math module as m

a=int (input("Enter a number: "))	# Taking input from the user

b=m.sqrt(a)				# sqrt is the function for square root
print(f"Square root: {b}")

c=m.log(a)				# log is the function of logaritm
print(f"Logaritm: {c}")

d=m.sin(a)				# sin is the function of sine
print(f"Sine: {d}")
